public class SwitchCaseHW {
    public static void main(String[] args) {
        char ch = 'b';
        switch (ch){
            case 'd':
            System.out.println("Case 1");
            break;

            case 'b':
                System.out.println("Case 2");
                break;

            case 'x':
                System.out.println("Case 3");
                break;

            case 'y':
                System.out.println("Case 4");
            default:
                System.out.println();
        }

    }
}
